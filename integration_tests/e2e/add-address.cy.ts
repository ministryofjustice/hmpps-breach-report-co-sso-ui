context('Add Address page', () => {
  it('page will load and show all required fields', () => {
    cy.visit('/add-address/cb43e340-883a-497a-aa30-48e37dfd1866')
    cy.url().should('include', '/add-address')
    cy.get('#officeDescription').should('exist').should('be.visible')
    cy.get('#buildingName').should('exist').should('be.visible')
    cy.get('#buildingNumber').should('exist').should('be.visible')
    cy.get('#streetName').should('exist').should('be.visible')
    cy.get('#district').should('exist').should('be.visible')
    cy.get('#townCity').should('exist').should('be.visible')
    cy.get('#county').should('exist').should('be.visible')
    cy.get('#postcode').should('exist').should('be.visible')
  })

  it('will save all fields and redirect properly when Save is clicked', () => {
    cy.visit('/add-address/2048be31-f7da-4a28-958c-6a9e0691c06a')
    cy.url().should('include', '/add-address')
    cy.get('#officeDescription').type('A Description')
    cy.get('#buildingName').type('B Building Name')
    cy.get('#buildingNumber').type('C House Number')
    cy.get('#streetName').type('D Street Name')
    cy.get('#district').type('E District')
    cy.get('#townCity').type('F Town City')
    cy.get('#county').type('G County')
    cy.get('#postcode').type('H')
    cy.get('#save-button').should('exist').should('be.visible')
    cy.get('#save-button').click()
    cy.url().should('include', '/witness-details')
  })

  it('will return to witness details screen when cancel is clicked', () => {
    cy.visit('/add-address/da1ad869-b29b-43e8-bfdc-fb15ce3934d7')
    cy.url().should('include', '/add-address')
    cy.get('#cancel-button').should('exist').should('be.visible')
    cy.get('#cancel-button').click()
    cy.url().should('include', '/witness-details')
  })

  it('validation will trigger when none of description, number or name is present', () => {
    cy.visit('/add-address/68cfc760-1d4d-458c-8db5-c84ec0179026')
    cy.url().should('include', '/add-address')
    cy.get('#streetName').type('D Street Name')
    cy.get('#district').type('E District')
    cy.get('#townCity').type('F Town City')
    cy.get('#county').type('G County')
    cy.get('#postcode').type('H Postcode')
    cy.get('#save-button').should('exist').should('be.visible')
    cy.get('#save-button').click()
    cy.get('.govuk-error-summary__title').should('exist').should('contain.text', 'There is a problem')
    cy.get('#officeDescription-error')
      .should('exist')
      .should('contain.text', 'At least 1 out of [Description, Building Name, Address Number] must be present')
    cy.get('#buildingName-error')
      .should('exist')
      .should('contain.text', 'At least 1 out of [Description, Building Name, Address Number] must be present')
    cy.get('#buildingNumber-error')
      .should('exist')
      .should('contain.text', 'At least 1 out of [Description, Building Name, Address Number] must be present')
  })

  it('validation will trigger when street, town or postcode is left blank', () => {
    cy.visit('/add-address/452750f1-dcfd-4d79-a269-09dfb1b9f7e4')
    cy.url().should('include', '/add-address')
    cy.get('#officeDescription').type('A Description')
    cy.get('#buildingName').type('B Building Name')
    cy.get('#buildingNumber').type('C House Number')
    cy.get('#district').type('E District')
    cy.get('#save-button').should('exist').should('be.visible')
    cy.get('#save-button').click()
    cy.get('.govuk-error-summary__title').should('exist').should('contain.text', 'There is a problem')
    cy.get('#streetName-error')
      .should('exist')
      .should('contain.text', 'Street Name : This is a required value, please enter a value')
    cy.get('#townCity-error')
      .should('exist')
      .should('contain.text', 'Town/City : This is a required value, please enter a value')
    cy.get('#postcode-error')
      .should('exist')
      .should('contain.text', 'Postcode : This is a required value, please enter a value')
  })

  it('page will load and show previously entered data fields', () => {
    cy.visit('/add-address/44fbdcf5-0e07-44b8-94aa-2e685798af70')
    cy.url().should('include', '/add-address')
    cy.get('#officeDescription').should('have.value', 'Descriptive Name')
    cy.get('#buildingName').should('have.value', 'Descriptive Tower')
    cy.get('#buildingNumber').should('have.value', '33')
    cy.get('#streetName').should('have.value', 'Descriptive Street')
    cy.get('#district').should('have.value', 'Descriptive District')
    cy.get('#townCity').should('have.value', 'Descriptive Town')
    cy.get('#county').should('have.value', 'Descriptive County')
    cy.get('#postcode').should('have.value', 'DE54 1BE')
  })

  it('validation will trigger when fields are over limits', () => {
    cy.visit('add-address/ee7140bc-ae6e-434f-980a-6c648d8248d5')
    cy.url().should('include', '/add-address')
    cy.get('#officeDescription').invoke('val', 'X'.repeat(51)).trigger('input')
    cy.get('#buildingName').invoke('val', 'X'.repeat(36)).trigger('input')
    cy.get('#buildingNumber').invoke('val', 'X'.repeat(36)).trigger('input')
    cy.get('#streetName').invoke('val', 'X'.repeat(36)).trigger('input')
    cy.get('#district').invoke('val', 'X'.repeat(36)).trigger('input')
    cy.get('#townCity').invoke('val', 'X'.repeat(36)).trigger('input')
    cy.get('#county').invoke('val', 'X'.repeat(36)).trigger('input')
    cy.get('#postcode').invoke('val', 'X'.repeat(9)).trigger('input')
    cy.get('#save-button').click()
    cy.get('.govuk-error-summary__title').should('exist').should('contain.text', 'There is a problem')
    cy.get('#officeDescription-error')
      .should('exist')
      .should('contain.text', 'Please enter 50 characters or less for Office Description')
    cy.get('#buildingName-error')
      .should('exist')
      .should('contain.text', 'Please enter 35 characters or less for Building Name')
    cy.get('#buildingNumber-error')
      .should('exist')
      .should('contain.text', 'Please enter 35 characters or less for Address Number')
    cy.get('#streetName-error')
      .should('exist')
      .should('contain.text', 'Please enter 35 characters or less for Street Name')
    cy.get('#townCity-error')
      .should('exist')
      .should('contain.text', 'Please enter 35 characters or less for Town or City')
    cy.get('#postcode-error').should('exist').should('contain.text', 'Please enter 8 characters or less for Postcode')
    cy.get('#district-error').should('exist').should('contain.text', 'Please enter 35 characters or less for District')
    cy.get('#county-error').should('exist').should('contain.text', 'Please enter 35 characters or less for County')
  })
})
